echo ""
echo "üîí –ì–∞—Ä–∞–Ω—Ç–∏—è –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç–∏:"
echo "   - –ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö –ù–ï —É–¥–∞–ª—è–µ—Ç—Å—è (reports.db, src/reports.db)"
echo "   - –¢–∞–±–ª–∏—Ü—ã Users –∏ UserSessions –∑–∞—â–∏—â–µ–Ω—ã (–≤–Ω—É—Ç—Ä–∏ –ë–î)"
echo "   - –°—É–ø–µ—Ä–ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–∏ –∑–∞—â–∏—â–µ–Ω—ã (–ø–æ–ª–µ is_superadmin –≤ –ë–î)"
echo "   - –°–µ—Å—Å–∏–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π –∑–∞—â–∏—â–µ–Ω—ã (—Ç–∞–±–ª–∏—Ü–∞ UserSessions)"
