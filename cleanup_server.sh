echo "   ‚úÖ –£–¥–∞–ª–µ–Ω—ã __pycache__ –∏ .pyc —Ñ–∞–π–ª—ã (–±–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö –∑–∞—â–∏—â–µ–Ω–∞)"
