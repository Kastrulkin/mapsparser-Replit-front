echo "   üîí –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è (.env —Ñ–∞–π–ª—ã) - –ó–ê–©–ò–©–ï–ù–ê"
echo "   ‚úÖ –¢–µ–∫—É—â–∞—è —Å–±–æ—Ä–∫–∞ (frontend/dist)"
