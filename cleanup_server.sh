echo "   - –ö–æ–¥ –ø—Ä–æ–µ–∫—Ç–∞ –ù–ï —É–¥–∞–ª—è–µ—Ç—Å—è (–≤–∫–ª—é—á–∞—è auth_system.py, user_api.py)"
echo "   - –ó–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –ù–ï —É–¥–∞–ª—è—é—Ç—Å—è (node_modules, venv)"
echo ""
echo "üîê –ê–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏—è –∏ –ª–∏—á–Ω—ã–π –∫–∞–±–∏–Ω–µ—Ç:"
