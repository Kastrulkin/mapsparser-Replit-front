#!/bin/bash
# –°–∫—Ä–∏–ø—Ç –±–µ–∑–æ–ø–∞—Å–Ω–æ–π –æ—á–∏—Å—Ç–∫–∏ —Å–µ—Ä–≤–µ—Ä–∞
# –£–¥–∞–ª—è–µ—Ç —Ç–æ–ª—å–∫–æ –Ω–µ–Ω—É–∂–Ω—ã–µ —Ñ–∞–π–ª—ã, —Å–æ—Ö—Ä–∞–Ω—è—è –≤–∞–∂–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ

set -e

echo "üßπ –ù–∞—á–∏–Ω–∞–µ–º –æ—á–∏—Å—Ç–∫—É —Å–µ—Ä–≤–µ—Ä–∞..."
echo ""

# –ü–µ—Ä–µ—Ö–æ–¥–∏–º –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –ø—Ä–æ–µ–∫—Ç–∞
cd /root/mapsparser-Replit-front || {
    echo "‚ùå –î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è –ø—Ä–æ–µ–∫—Ç–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞!"
    exit 1
}

# –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Ç–µ–∫—É—â–µ–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ –¥–∏—Å–∫–∞
echo "üìä –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ –¥–∏—Å–∫–∞ –î–û –æ—á–∏—Å—Ç–∫–∏:"
df -h / | tail -1
echo ""

# 1. –û—á–∏—Å—Ç–∫–∞ –∫–µ—à–µ–π npm
echo "üóëÔ∏è  –û—á–∏—Å—Ç–∫–∞ –∫–µ—à–µ–π npm..."
cd frontend
if [ -d "node_modules/.cache" ]; then
    rm -rf node_modules/.cache
    echo "   ‚úÖ –£–¥–∞–ª–µ–Ω node_modules/.cache"
fi
npm cache clean --force 2>/dev/null || true
cd ..
echo ""

# 2. –û—á–∏—Å—Ç–∫–∞ Python –∫–µ—à–µ–π
echo "üóëÔ∏è  –û—á–∏—Å—Ç–∫–∞ Python –∫–µ—à–µ–π..."
find . -type d -name "__pycache__" -exec rm -rf {} + 2>/dev/null || true
find . -type f -name "*.pyc" -delete 2>/dev/null || true
echo "   ‚úÖ –£–¥–∞–ª–µ–Ω—ã __pycache__ –∏ .pyc —Ñ–∞–π–ª—ã"
echo ""

# 3. –û—á–∏—Å—Ç–∫–∞ —Å—Ç–∞—Ä—ã—Ö backup —Ñ–∞–π–ª–æ–≤ (–æ—Å—Ç–∞–≤–ª—è–µ–º —Ç–æ–ª—å–∫–æ –ø–æ—Å–ª–µ–¥–Ω–∏–µ 3)
echo "üóëÔ∏è  –û—á–∏—Å—Ç–∫–∞ —Å—Ç–∞—Ä—ã—Ö backup —Ñ–∞–π–ª–æ–≤..."
if [ -d "db_backups" ]; then
    # –û—Å—Ç–∞–≤–ª—è–µ–º —Ç–æ–ª—å–∫–æ –ø–æ—Å–ª–µ–¥–Ω–∏–µ 3 –±—ç–∫–∞–ø–∞
    cd db_backups
    ls -t *.backup 2>/dev/null | tail -n +4 | xargs rm -f 2>/dev/null || true
    cd ..
    echo "   ‚úÖ –û—Å—Ç–∞–≤–ª–µ–Ω—ã —Ç–æ–ª—å–∫–æ –ø–æ—Å–ª–µ–¥–Ω–∏–µ 3 –±—ç–∫–∞–ø–∞ –ë–î"
fi

# –£–¥–∞–ª—è–µ–º .backup —Ñ–∞–π–ª—ã –∏–∑ src/
find src -name "*.backup" -type f -delete 2>/dev/null || true
echo "   ‚úÖ –£–¥–∞–ª–µ–Ω—ã .backup —Ñ–∞–π–ª—ã"
echo ""

# 4. –û—á–∏—Å—Ç–∫–∞ —Å—Ç–∞—Ä—ã—Ö –ª–æ–≥–æ–≤
echo "üóëÔ∏è  –û—á–∏—Å—Ç–∫–∞ –ª–æ–≥–æ–≤..."
# –õ–æ–≥–∏ –ø—Ä–æ–µ–∫—Ç–∞
rm -f *.log 2>/dev/null || true
rm -f frontend/*.log 2>/dev/null || true
rm -f src/*.log 2>/dev/null || true
rm -f server.out 2>/dev/null || true
rm -f frontend/server.out 2>/dev/null || true
echo "   ‚úÖ –£–¥–∞–ª–µ–Ω—ã –ª–æ–≥–∏ –ø—Ä–æ–µ–∫—Ç–∞"

# –°–∏—Å—Ç–µ–º–Ω—ã–µ –ª–æ–≥–∏ (–æ—Å—Ç–∞–≤–ª—è–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–µ 7 –¥–Ω–µ–π)
journalctl --vacuum-time=7d >/dev/null 2>&1 || true
echo "   ‚úÖ –û—á–∏—â–µ–Ω—ã —Å–∏—Å—Ç–µ–º–Ω—ã–µ –ª–æ–≥–∏ (–æ—Å—Ç–∞–≤–ª–µ–Ω—ã –ø–æ—Å–ª–µ–¥–Ω–∏–µ 7 –¥–Ω–µ–π)"
echo ""

# 5. –û—á–∏—Å—Ç–∫–∞ –≤—Ä–µ–º–µ–Ω–Ω—ã—Ö —Ñ–∞–π–ª–æ–≤
echo "üóëÔ∏è  –û—á–∏—Å—Ç–∫–∞ –≤—Ä–µ–º–µ–Ω–Ω—ã—Ö —Ñ–∞–π–ª–æ–≤..."
rm -rf /tmp/* 2>/dev/null || true
rm -rf /var/tmp/* 2>/dev/null || true
echo "   ‚úÖ –û—á–∏—â–µ–Ω—ã /tmp –∏ /var/tmp"
echo ""

# 6. –û—á–∏—Å—Ç–∫–∞ —Å—Ç–∞—Ä—ã—Ö –ª–æ–≥–æ–≤ nginx (–æ—Å—Ç–∞–≤–ª—è–µ–º —Å—Ç—Ä—É–∫—Ç—É—Ä—É, –Ω–æ –æ—á–∏—â–∞–µ–º —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ)
echo "üóëÔ∏è  –û—á–∏—Å—Ç–∫–∞ –ª–æ–≥–æ–≤ nginx..."
if [ -f /var/log/nginx/access.log ]; then
    truncate -s 0 /var/log/nginx/access.log 2>/dev/null || true
fi
if [ -f /var/log/nginx/error.log ]; then
    truncate -s 0 /var/log/nginx/error.log 2>/dev/null || true
fi
echo "   ‚úÖ –û—á–∏—â–µ–Ω—ã –ª–æ–≥–∏ nginx"
echo ""

# 7. –û—á–∏—Å—Ç–∫–∞ –ø–∞–∫–µ—Ç–æ–≤ apt
echo "üóëÔ∏è  –û—á–∏—Å—Ç–∫–∞ –ø–∞–∫–µ—Ç–æ–≤ apt..."
apt clean >/dev/null 2>&1 || true
apt autoremove -y >/dev/null 2>&1 || true
echo "   ‚úÖ –û—á–∏—â–µ–Ω—ã –ø–∞–∫–µ—Ç—ã apt"
echo ""

# 8. –ü–æ–∫–∞–∑—ã–≤–∞–µ–º, —á—Ç–æ –∑–∞–Ω–∏–º–∞–µ—Ç –±–æ–ª—å—à–µ –≤—Å–µ–≥–æ –º–µ—Å—Ç–∞ (—Ç–æ–ø-10)
echo "üìä –¢–æ–ø-10 —Å–∞–º—ã—Ö –±–æ–ª—å—à–∏—Ö –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–π –≤ /root:"
du -sh /root/* 2>/dev/null | sort -h | tail -10
echo ""

# 9. –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ –¥–∏—Å–∫–∞ –ü–û–°–õ–ï –æ—á–∏—Å—Ç–∫–∏
echo "üìä –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ –¥–∏—Å–∫–∞ –ü–û–°–õ–ï –æ—á–∏—Å—Ç–∫–∏:"
df -h / | tail -1
echo ""

# 10. –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Ä–∞–∑–º–µ—Ä –ø—Ä–æ–µ–∫—Ç–∞
echo "üì¶ –†–∞–∑–º–µ—Ä –ø—Ä–æ–µ–∫—Ç–∞:"
du -sh /root/mapsparser-Replit-front
echo ""

echo "‚úÖ –û—á–∏—Å—Ç–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞!"
echo ""
echo "üíæ –°–æ—Ö—Ä–∞–Ω–µ–Ω–æ:"
echo "   ‚úÖ –ö–æ–¥ –ø—Ä–æ–µ–∫—Ç–∞ (git)"
echo "   ‚úÖ –ó–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ (node_modules, venv)"
echo "   ‚úÖ –ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö (reports.db, src/reports.db)"
echo "   ‚úÖ –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è (.env —Ñ–∞–π–ª—ã)"
echo "   ‚úÖ –¢–µ–∫—É—â–∞—è —Å–±–æ—Ä–∫–∞ (frontend/dist)"
echo "   ‚úÖ –ü–æ—Å–ª–µ–¥–Ω–∏–µ 3 –±—ç–∫–∞–ø–∞ –ë–î"
echo ""

