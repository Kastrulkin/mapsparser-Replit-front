    ls -t *.backup 2>/dev/null | tail -n +4 | xargs rm -f 2>/dev/null || true
    cd ..
    echo "   ‚úÖ –û—Å—Ç–∞–≤–ª–µ–Ω—ã —Ç–æ–ª—å–∫–æ –ø–æ—Å–ª–µ–¥–Ω–∏–µ 3 –±—ç–∫–∞–ø–∞ –ë–î"
